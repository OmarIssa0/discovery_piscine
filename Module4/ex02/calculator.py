#! /usr/bin/env python3

input_num1 = input("Give me the first number: ")
num1 = int(input_num1)
input_num2 = input("Give me the second number: ")
num2 = int(input_num2)
print(f"{num1} + {num2} = {num1 + num2}")
print(f"{num1} - {num2} = {num1 - num2}")
print(f"{num1} / {num2} = {num1 // num2}")
print(f"{num1} * {num2} = {num1 * num2}")